<h2>What is HSK?</h2> <br/>

   <p class="slheight cbgcoll">HSK is an acronym for Hanyu (Chinese) Shuiping (level) Kaoshi (Test). The HSK literally means Chinese level test or Mandarin level test. It is a proficiency test started by Koryo Kikin and the China International Education Association in China in 1984. The test measures your proficiency in the Chinese language through listening, reading, and writing skills.
</p>

   <p class="slheight cbgcoll">HSK tests are held across the globe on a regular basis. Candidates who qualify the test are awarded the admissible level of Chinese Proficiency Certificate. There are certain levels of HSK-
</p>



   <p class="slheight cbgcoll">HSK level-1- This is the basic level of HSK. In this level, students can recognize and use some fundamental characters. The students can understand and say some simple phrases.</p>


   <p class="slheight cbgcoll">HSK level-2- Students can apply the Chinese language in day-to-day situations. </p>


   <p class="slheight cbgcoll">HSK level-3- Student can easily communicate when traveling to China. He/she can complete day-to-day tasks in everyday life, at schools and at work.</p>

   <p class="slheight cbgcoll">
     HSK level-4- Students can speak on a broad range of topics in Chinese language and can communicate with native speakers.
   </p>

   <p class="slheight cbgcoll">
     HSK level-5- After completing this level, students can read Chinese newspapers, books, watch Chinese movies, make long presentations using the Chinese language.
   </p>

   <p class="slheight cbgcoll">
     HSK level-6- This is the final level of this test. After qualifying this, the student can easily understand any type of information in Chinese. Also, they can express themselves freely in both oral and written form.
